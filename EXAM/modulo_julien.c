#include <unistd.h>

int	main(int argc, char **argv)
{
	int	i;
	int	j;
	
	i = 0;
	j = 1;
	while (j < argc)
	{
		i = 0;
		if (j % 2 == 0)
		{
			while (argv[j][i])
			{
				write (1, &argv[j][i], 1);
				i++;
			}
		}
		j++;
	}
	return (0);
}
